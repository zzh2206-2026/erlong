//-------------------------------------------------------------------------------------------------------------------
//【文件名】                 Task.hpp
//【功能模块和目的】         任务抽象基类，定义任务通用属性与 CPM 调度时间字段
//-------------------------------------------------------------------------------------------------------------------

#ifndef TASK_HPP
#define TASK_HPP

#include <string>
#include <memory>

//-------------------------------------------------------------------------------------------------------------------
//【类名】             Task
//【功能】             项目任务的抽象基类，封装名称、工期及 CPM 时间参数
//【接口说明】         提供任务基本属性访问、类型判断及克隆接口
//-------------------------------------------------------------------------------------------------------------------
class Task {
public:
    //-------------------------------------------------------------------------------------------------------------------
    //【函数名称】       Task
    //【函数功能】       构造任务对象
    //【参数】           strName - 输入，任务名称
    //                   iDuration - 输入，任务工期（天）
    //                   iFileId - 输入，PPM 文件中的任务 ID
    //【返回值】         无
    //-------------------------------------------------------------------------------------------------------------------
    Task(const std::string& strName, int iDuration, int iFileId);

    virtual ~Task() = default;

    //-------------------------------------------------------------------------------------------------------------------
    //【函数名称】       GetName
    //【函数功能】       获取任务名称
    //【参数】           无
    //【返回值】         任务名称字符串
    //-------------------------------------------------------------------------------------------------------------------
    const std::string& GetName() const;

    //-------------------------------------------------------------------------------------------------------------------
    //【函数名称】       SetName
    //【函数功能】       设置任务名称
    //【参数】           strName - 输入，新名称
    //【返回值】         无
    //-------------------------------------------------------------------------------------------------------------------
    void SetName(const std::string& strName);

    //-------------------------------------------------------------------------------------------------------------------
    //【函数名称】       GetDuration
    //【函数功能】       获取任务工期
    //【参数】           无
    //【返回值】         工期天数
    //-------------------------------------------------------------------------------------------------------------------
    virtual int GetDuration() const;

    //-------------------------------------------------------------------------------------------------------------------
    //【函数名称】       SetDuration
    //【函数功能】       设置任务工期
    //【参数】           iDuration - 输入，新工期
    //【返回值】         无
    //-------------------------------------------------------------------------------------------------------------------
    virtual void SetDuration(int iDuration);

    //-------------------------------------------------------------------------------------------------------------------
    //【函数名称】       GetFileId
    //【函数功能】       获取 PPM 文件中的任务 ID
    //【参数】           无
    //【返回值】         文件 ID
    //-------------------------------------------------------------------------------------------------------------------
    int GetFileId() const;

    //-------------------------------------------------------------------------------------------------------------------
    //【函数名称】       SetFileId
    //【函数功能】       设置 PPM 文件中的任务 ID
    //【参数】           iFileId - 输入，文件 ID
    //【返回值】         无
    //-------------------------------------------------------------------------------------------------------------------
    void SetFileId(int iFileId);

    //-------------------------------------------------------------------------------------------------------------------
    //【函数名称】       IsMilestone
    //【函数功能】       判断是否为里程碑任务
    //【参数】           无
    //【返回值】         是里程碑返回 true
    //-------------------------------------------------------------------------------------------------------------------
    virtual bool IsMilestone() const = 0;

    //-------------------------------------------------------------------------------------------------------------------
    //【函数名称】       GetTypeName
    //【函数功能】       获取任务类型名称
    //【参数】           无
    //【返回值】         类型描述字符串
    //-------------------------------------------------------------------------------------------------------------------
    virtual std::string GetTypeName() const = 0;

    //-------------------------------------------------------------------------------------------------------------------
    //【函数名称】       Clone
    //【函数功能】       克隆当前任务对象
    //【参数】           无
    //【返回值】         新任务对象的智能指针
    //-------------------------------------------------------------------------------------------------------------------
    virtual std::unique_ptr<Task> Clone() const = 0;

    // CPM 调度时间参数访问接口
    int GetES() const;
    int GetEF() const;
    int GetLS() const;
    int GetLF() const;
    void SetES(int iES);
    void SetEF(int iEF);
    void SetLS(int iLS);
    void SetLF(int iLF);
    void ResetSchedule() const;

protected:
    std::string m_strName;   // 任务名称
    int m_iDuration;         // 任务工期（天）
    int m_iFileId;           // PPM 文件中的任务 ID

    // CPM 计算结果，mutable 以便在 const 推理方法中写入
    mutable int m_iES;       // 最早开始时间
    mutable int m_iEF;       // 最早完成时间
    mutable int m_iLS;       // 最晚开始时间
    mutable int m_iLF;       // 最晚完成时间
};

#endif
