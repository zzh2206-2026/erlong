//-------------------------------------------------------------------------------------------------------------------
//【文件名】                 Exporter.hpp
//【功能模块和目的】         项目模型导出器抽象基类
//-------------------------------------------------------------------------------------------------------------------

#ifndef EXPORTER_HPP
#define EXPORTER_HPP

#include <string>

class Project;

//-------------------------------------------------------------------------------------------------------------------
//【类名】             Exporter
//【功能】             定义项目模型文件导出的抽象接口
//【接口说明】         派生类实现具体格式的导出逻辑
//-------------------------------------------------------------------------------------------------------------------
class Exporter {
public:
    virtual ~Exporter() = default;

    //-------------------------------------------------------------------------------------------------------------------
    //【函数名称】       Export
    //【函数功能】       将内存中的项目模型导出到文件
    //【参数】           strFileName - 输入，文件路径
    //                   project - 输入，源项目对象
    //【返回值】         导出成功返回 true
    //-------------------------------------------------------------------------------------------------------------------
    virtual bool Export(const std::string& strFileName, const Project& project) const = 0;
};

#endif
