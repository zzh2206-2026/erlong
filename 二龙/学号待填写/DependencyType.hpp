//-------------------------------------------------------------------------------------------------------------------
//【文件名】                 DependencyType.hpp
//【功能模块和目的】         定义任务依赖关系的四种时序类型枚举
//-------------------------------------------------------------------------------------------------------------------

#ifndef DEPENDENCY_TYPE_HPP
#define DEPENDENCY_TYPE_HPP

#include <string>

//-------------------------------------------------------------------------------------------------------------------
//【类名】             DependencyType
//【功能】             封装 FS/SS/FF/SF 四种依赖时序逻辑类型及其字符串转换
//【接口说明】         提供类型枚举、字符串解析与格式化接口
//-------------------------------------------------------------------------------------------------------------------
class DependencyType {
public:
    // 依赖时序类型枚举
    enum class Type {
        FS,  // 完成-开始 Finish-to-Start
        SS,  // 开始-开始 Start-to-Start
        FF,  // 完成-完成 Finish-to-Finish
        SF   // 开始-完成 Start-to-Finish
    };

    //-------------------------------------------------------------------------------------------------------------------
    //【函数名称】       ToString
    //【函数功能】       将依赖类型枚举转换为 PPM 格式字符串
    //【参数】           type - 输入，依赖类型枚举值
    //【返回值】         对应的类型字符串（FS/SS/FF/SF）
    //-------------------------------------------------------------------------------------------------------------------
    static std::string ToString(Type type);

    //-------------------------------------------------------------------------------------------------------------------
    //【函数名称】       FromString
    //【函数功能】       从 PPM 格式字符串解析依赖类型
    //【参数】           strType - 输入，类型字符串
    //【返回值】         解析成功返回对应枚举，失败返回 false 并通过 type 输出
    //-------------------------------------------------------------------------------------------------------------------
    static bool FromString(const std::string& strType, Type& type);
};

#endif
