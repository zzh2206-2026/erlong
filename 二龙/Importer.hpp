//【功能模块和目的】         项目模型导入器抽象基类
//-------------------------------------------------------------------------------------------------------------------

#ifndef IMPORTER_HPP
#define IMPORTER_HPP

#include <string>

class Project;

//-------------------------------------------------------------------------------------------------------------------
//【类名】             Importer
//【功能】             定义项目模型文件导入的抽象接口
//【接口说明】         派生类实现具体格式的导入逻辑
//-------------------------------------------------------------------------------------------------------------------
class Importer {
public:
    virtual ~Importer() = default;

    //-------------------------------------------------------------------------------------------------------------------
    //【函数名称】       Import
    //【函数功能】       从文件导入项目模型到内存
    //【参数】           strFileName - 输入，文件路径
    //                   project - 输出，目标项目对象
    //【返回值】         导入成功返回 true
    //-------------------------------------------------------------------------------------------------------------------
    virtual bool Import(const std::string& strFileName, Project& project) const = 0;
};

#endif
